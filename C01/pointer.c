#include<stdio.h>

int	main(void)
{
	int a;
	int *b;

	a=0;
	b=&a;
	*b=42;
	printf("%d\n", a);
	printf("%d\n", *b);
	printf("%x\n", &a);
	printf("%x\n", b);
	return (0);
}
